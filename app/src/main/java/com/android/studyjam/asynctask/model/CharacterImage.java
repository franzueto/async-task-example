package com.android.studyjam.asynctask.model;

public class CharacterImage {

    public String name;
    public String url;

    public CharacterImage() {

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
